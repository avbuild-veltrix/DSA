#include<iostream>
using namespace std;
int main(){
    int x = 1, y = 4, z = 7;
    //Pre Increment & Post Decrement
    cout<<++x<<endl;;
    cout<< x++ <<endl;
    //Pre Decrement & Post Decrement
    cout<<--y<<endl;
    cout<<y--;
    ///////////////////////////////////
    cout<<++z<<" "<< z++ <<endl;
    cout<<--y<<" "<<y--;
}
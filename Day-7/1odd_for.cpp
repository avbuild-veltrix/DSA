#include<iostream>
using namespace std;
int main(){
    //Printing odd numbers using for loop
    cout<<"Printing Odd numbers using for loop"<<endl;
    for(int i =1; i<=100;i+=2){
        cout<<i<<" ";
    }

    cout<<endl<<"Printing Even numbers using for loop"<<endl;
    //Printing Even numbers using for loop
    for(int i =2; i<=100;i+=2){
        cout<<i<<" ";
    }
}
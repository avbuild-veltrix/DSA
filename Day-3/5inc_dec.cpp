#include<iostream>
using namespace std;
int main(){
    int a = 6,b = 4,c=9;
    cout<<"The pre-increment operator on a gives "<<++a<<endl;
    cout<<"The post-increment operator on a gives "<<b++<<endl;
    cout<<"The pre-decrement operator on a gives "<<--b<<endl;
    cout<<"The post-decrement operator on a gives "<<c--<<endl;
}